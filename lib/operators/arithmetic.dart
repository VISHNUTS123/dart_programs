void main() {
  var a = 101;
  var b = 2;


  print('sum = ${a+b}');

  print('difference = ${a-b} ');

  print('quotient = : ${a/b}');
  print('remainder = : ${a%b}');
  print('prefix value increment of a:${a++}');
  print('postfix value increment of a:${++a}');
  print('postfix value decrement of b:${b--}');
  print('prefix value decrement of b:${--b}');   //The postfix value


}
